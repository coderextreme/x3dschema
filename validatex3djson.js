function validateFile(file) {
	try {
		var hello = JSON.parse(fs.readFileSync(file).toString());
		var version = hello["X3D"]["@version"];

		var valid = ajvValidate[version](hello);
		if (!valid) {
		    console.log('Ajv '+version+' Validation failed on', file);
		    console.log('Ajv '+version+' Validation errors:');
		    var errs = ajvValidate[version].errors;
		    for (var e in errs) {

			    console.log(" dataPath:", errs[e].dataPath);
			    console.log(" schemaPath:", errs[e].schemaPath);
			    console.log(" message:", errs[e].message);
			    console.log(" params:", errs[e].params);
			    console.log(" data:", errs[e].data);
			    console.log("------------------------");
		    }
		} else {
		    // console.log('Ajv '+version+' Valid');
		}
		/////////////////////////////

		var errors = jjvEnv[version].validate('x3djson', hello);

		if (!errors) {
			// console.log('jjv '+version+' Valid');
		} else {
			console.log('jjv '+version+' Validation failed on', file);
			console.log('jjv '+version+' Validation errors:');
			console.log(JSON.stringify(errors));
		}
		//////////////////////////////

		var valid2 = imjvValidate[version](hello);

		if (valid2) {
		    // console.log('is-my-json-valid '+version+' Valid');
		} else {
		    console.log('is-my-json-valid '+version+' Validation failed on', file);
		    console.log('is-my-json-valid '+version+' Validation errors:');
		    console.log(JSON.stringify(imjvValidate[version].errors, null, 1));
		}
	} catch (e) {
		console.log('Failed JSON parse', file, e);
	}
}

